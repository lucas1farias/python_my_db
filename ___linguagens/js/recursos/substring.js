// !@# (substring(i1, i2)) exibir caracteres de uma classe [ string ] de forma filtrada

const caracteres = '0123'

// Como eu devo pensar esse método com 1 parâmetro? (minha opinião)
console.log(caracteres.substring(0)) // mostre-me tudo, como uma impressão normal
console.log(caracteres.substring(1)) // mostre-me chars a partir do índice 1
console.log(caracteres.substring(2)) // mostre-me chars a partir do índice 2

// Como eu devo pensar esse método com 2 parâmetros? (minha opinião)
console.log(caracteres.substring(1, 3)) // mostre-me chars a partir do índice 1, até 3 - 1
